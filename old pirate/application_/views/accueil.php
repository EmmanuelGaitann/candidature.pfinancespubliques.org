                                       <center><strong><h2> NOTE D’INFORMATION </h2></strong></center>
									   <p>
											<strong><span style="font-style:italique">Le Ministre des Finances et le Recteur de l’Université de Yaoundé II-Soa lancent un appel à candidatures pour la 3ème promotion du Programme Supérieur de Spécialisation en Finances Publiques (PSSFP).</span></strong>
									   </p>
									   
									   <p style="font-size: 14px;">
											Dans le cadre du renforcement des capacités des personnels de l’Etat dans le domaine des Finances Publiques, 
											<strong>Le Ministre des Finances, Alamine Ousmane Mey et le Recteur de l’Université de Yaoundé II-Soa, le Pr. Ibrahima Adamou </strong>
											lancent un appel à candidature pour l’admission en 1ère année du cycle de Master spécialisé en finances publiques, au titre de l'année académique
                                                                                        2015-2016, dans les filières ci après du Programme Supérieur de Spécialisation en Finances Publiques :<br/><br/>
                                                                                        
                                                                                        <strong>I-	Formation en présentiel</strong>
											<ul style="font-size: 14px;">
												<li><strong>Comptabilité Publique :</strong> 20 places  </li>
												<li><strong>Gestion Budgétaire    :</strong>  20 places  </li>
												<li><strong>Finances Publiques Locales :</strong>  20 places  </li>
												<li><strong>Fiscalité  :</strong>  20 places  </li>
												<li><strong>Contrôle et Audit des Finances Publiques, Jugement des Comptes Publics</strong>  20 places  </li>

											</ul>
                                                                                        
                                                                                        <strong>II-	Formation à distance</strong>
											<ul style="font-size: 14px;">
												<li><strong>Comptabilité Publique :</strong> 10 places  </li>
												<li><strong>Gestion Budgétaire    :</strong>  10 places  </li>
												<li><strong>Finances Publiques Locales :</strong>  10 places  </li>
												<li><strong>Fiscalité  :</strong>  20 places  </li>
												<li><strong>Contrôle et Audit des Finances Publiques, Jugement des Comptes Publics</strong>  10 places  </li>

											</ul>
										
										</p>
                                                                                <center><strong> A)   CONDITIONS A REMPLIR  </strong></center>
                                                                                
                                                                                <p style="font-size: 14px;">
                                                                                    1-	Être titulaire d’un diplôme universitaire de niveau BAC + 3 avec une formation en économie, gestion, droit et disciplines affinitaires.<br/>
                                                                                    2-	La validation des acquis de l’expérience peut être portée en fonction de la cohérence entre la formation et le projet professionnel du candidat. Le cas échéant, une expérience de cinq ans (5 ans) est requise.

                                                                                </p>
                                                                                
                                                                                <center><strong> B)   DOSSIERS DE CANDITATURE   </strong></center>
                                                                                
                                                                                <p style="font-size: 14px;">
                                                                                    Les dossiers de candidature complets doivent parvenir au bureau de la scolarité du PSSFP à Yaoundé, au plus tard <br/>le <span style="color: red; font-size:20px;">30 novembre 2015 </span>. Ils doivent obligatoirement comporter les pièces suivantes :
                                                                                    <br/>
                                                                                    <br/>
                                                                                    <strong>Pour tous les candidats</strong>
                                                                                    <br/>
                                                                                <ul style="font-size: 14px; ">
                                                                                    <li style="margin-bottom: 5px;">01 fiche individuelle de candidature à retirer au bureau de la scolarité du PSSFP, ou à partir du site internet du PSSFP dont l’adresse est <a href="http://www.pfinancespubliques.org"> www.pfinancespubliques.org</a></li>
                                                                                    <li style="margin-bottom: 5px;">01 copie certifiée conforme des diplômes exigés, datant de moins de trois mois ;</li>
                                                                                    <li style="margin-bottom: 5px;">01 copie d’acte de naissance légalisée (cf. fiche de candidature) ;</li>
                                                                                    <li style="margin-bottom: 5px;">Les relèves de note de cycle de licence ;</li>
                                                                                    <li style="margin-bottom: 5px;">Un CV détaillé ;</li>
                                                                                    <li style="margin-bottom: 5px;">Une lettre de motivation ;</li>
                                                                                    <li style="margin-bottom: 5px;">Une attestation de service et une autorisation de l’employeur seront requises le cas échéant ;</li>
                                                                                    <li style="margin-bottom: 5px;">01 grande enveloppe timbrée au tarif réglementaire et portant l’adresse du candidat ;</li>
                                                                                    <li style="margin-bottom: 5px;">Le récépissé de versement au Crédit Mutuel d’Investissement, d’une somme de FCFA <strong>50 000</strong> au titre des frais de dépôt du dossier, ou d’un mandat –lettre adressé à monsieur le Responsable Administratif et Financier du PSSFP, BP .16578 Yaoundé-Messa.</li>
                                                                                 </ul>
                                                                               </p>
                                                                               <p style="font-size: 14px;">
                                                                                   <strong><u>NB :</u></strong> <a href="http://candidature.pfinancespubliques.org/index.php/candidature/add">Un formulaire d’inscription en ligne</a> est également disponible sur le site internet du PSSFP et à la scolarité, porte 231. 
                                                                               </p>
                                                                                
                                                                                 <center><strong> C)   SELECTION   </strong></center>
                                                                                
                                                                                <p style="font-size: 14px;">
                                                                                    1.	Etude de dossier et entretien devant le jury pour les candidats ayant souscrit à la modalité du présentiel ;<br/>
                                                                                    2.	Etude de dossier pour les candidats ayant souscrit à la modalité de la formation à distance.
                                                                                </p>
                                                                                
